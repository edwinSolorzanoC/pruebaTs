//BOCAS-----------------------------------------------------------------------
document.getElementById('abrirBocas').addEventListener('click', function() {
    document.getElementById('panelBocas').style.display = 'block';
});

document.getElementById('panelBocas').addEventListener('click', function(e) {
    if (e.target === this) {
        document.getElementById('panelBocas').style.display = 'none';
    }
});
document.getElementById('abrirBocas').addEventListener('click', function(event) {
    event.preventDefault(); // Evitar que la página se desplace al inicio
    document.getElementById('panelBocas').style.display = 'block';
});
document.getElementById('cerrarBocas').addEventListener('click', function() {
    document.getElementById('panelBocas').style.display = 'none';
});
document.getElementById('cerrarBocas').addEventListener('click', function(event) {
    event.preventDefault(); // Evitar que la página se desplace al inicio
    document.getElementById('panelBocas').style.display = 'none';
});

